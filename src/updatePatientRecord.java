    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String patientID = jTextField1.getText();
        String discharge = jTextField5.getText();
        if(discharge.compareTo("false")==0)
        {
        String discharged = "true";
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("update patient set discharged='"+discharged+"' where patientID = '"+patientID+"'");
            JOptionPane.showMessageDialog(null, "Thankyou for visiting us. Stay safe and healthy!");
            setVisible(false);
            new updatePatientRecord().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
        else if(patientID.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please search for a patient.");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Patient has already been discharged.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

